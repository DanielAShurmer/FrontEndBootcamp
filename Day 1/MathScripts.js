function adder(InputA, InputB){
    console.log(parseInt(InputA) + parseInt(InputB));
}

function subtracter(InputA, InputB){
    console.log(parseInt(InputA) - parseInt(InputB));
}

function multiplier(InputA, InputB){
    console.log(parseInt(InputA) * parseInt(InputB));
}

function divider(InputA, InputB){
    console.log(parseInt(InputA) / parseInt(InputB));
}

function moduluser(InputA, InputB){
    console.log(parseInt(InputA) % parseInt(InputB));
}
